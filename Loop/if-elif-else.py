score=87

if score==100 :
    print("A+") # score等於100印出A+
elif 90<=score<100 :
    print("A") # score介於90~99印出A
elif 80<=score<90 :
    print("B") # score介於80~89印出B
elif 70<=score<80 :
    print("C") # score介於70~79印出C
elif 60<=score<70 :
    print("D") # score介於60~69印出D
else :
    print("F") # score不在以上範圍內印出F


